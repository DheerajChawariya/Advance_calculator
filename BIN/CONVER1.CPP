/*WAP for number system*/
#include<stdio.h>
#include<conio.h>
void main()
{
clrscr();
printf("%d",12);  //decimal (12)10
printf("\n%d",012); //octel to decimal  (12)8=()10
printf("\n%d",0x12); //hexa to decimal  (12)16=()10
printf("\n%o",12);  //decimal to octel   (12)10=()8
printf("\n%o",012);  //octel           (12)8
printf("\n%x",0x12); //hexadecimal     (12)16
printf("\n%x",012);  //octel to hexadecimal  (12)8=()16
printf("\n%o",0x12); //hexa to octel   (12)16=()8
printf("\n%X",12);  //decimal to hexadecimal   (12)10=()16
getch();
}
